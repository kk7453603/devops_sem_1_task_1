# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ gunicorn –¥–ª—è –ø—Ä–æ–¥–∞–∫—à–µ–Ω–∞
if [ "$DEBUG" = "false" ]; then
    echo "üè≠ –ó–∞–ø—É—Å–∫ –≤ –ø—Ä–æ–¥–∞–∫—à–µ–Ω —Ä–µ–∂–∏–º–µ —Å gunicorn..."
    exec gunicorn \
        --bind $APP_HOST:$APP_PORT \
        --workers 4 \
        --worker-class sync \
        --log-level info \
        --access-logfile /opt/myapp/logs/access.log \
        --error-logfile /opt/myapp/logs/error.log \
        --capture-output \
        --enable-redirect-access \
